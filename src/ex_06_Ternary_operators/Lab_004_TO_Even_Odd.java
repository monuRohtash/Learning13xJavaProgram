package ex_06_Ternary_operators;

public class Lab_004_TO_Even_Odd {
    public static void main(String[] args) {
        // The even  number between two numbers by using ternary operator.

        int num = 2;
        String even = (num%2 == 0) ? "Even" : "Odd";
        System.out.println(even);



        // The Odd  number between two numbers by using ternary operator.

        int num1 = 1;
        String odd = (num1%2 == 0) ? "Even" : "Odd";
        System.out.println(odd);

    }
}
