package ex_10_Switch_Statement;

public class Lab_010_interview {
    public static void main(String[] args) {

        char code = 'B';

        switch (code) {
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
            default:
                System.out.println("Default");



        }

    }
}
