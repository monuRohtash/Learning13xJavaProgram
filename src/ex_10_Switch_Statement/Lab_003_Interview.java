package ex_10_Switch_Statement;

public class Lab_003_Interview {

    public static void main(String[] args) {

        int a = 5;
        switch (a){

            case 1:
                System.out.println("run case 1");

                break;
            case 2:
                System.out.println("run case 2");
                break;
            default:
                System.out.println("run default value");

        }


    }
}
