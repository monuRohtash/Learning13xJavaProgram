package ex_02_Variables_and_Data_Types;

public class Lab011_Constents_final_key {

    public static void main(String[] args) {
         int i = 10;
          i = 20;   // normal value it can change any time variable name
          System.out.println(i);



           final int a = 100;    //Using final key word before variable never change this value
           System.out.println(a);

    }
}
