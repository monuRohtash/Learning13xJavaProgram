package ex_13_Do_While_loop;

public class Lab_001_Do_While_loop {

    public static void main(String[] args) {

//        🔁 do-while Loop:
//             Body is executed first, then the condition is checked.

        int a = 0;
        do {
            System.out.println("This is a do- while loop :-> " + a);
            a++;
        }while (a < 10);






    }}
