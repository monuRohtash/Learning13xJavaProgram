package ex_11_For_Loop;

public class Lab_001_for_loop {

    public static void main(String[] args) {
        //for loop
        //help you to repeat a block of code.

//        Loops are used to repeat a block of code multiple times until a condition is met. The two most common
//        types of loops are for and while.





        // Initialization -> condation -> Updation (incre / decrese)
        // i - int
        // c - condation
        // u - updation

        //ICU

        for (int i=0; i < 10; i++){
            System.out.println(i);   // 0 to 9, times rum : 10

        }




    }

}
