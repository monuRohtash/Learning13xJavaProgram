package ex_04_Non_Primitive_Literals__Operators;

public class Lab006_Relational_Operators {
    public static void main(String[] args) {

        //  List of Relational Operators
        //  == Equal to (Checking )         5 == 5	            true
        // !=	Not Equal                   5 != 3          	true
        //  > Greater than                  7 > 4	            true
        //  < Less Then                     3 < 5	            true
        //  >= Greater then or equal to     5 >= 5	            true
        //  <= Less Then or Equal to        4 <= 6           	true

        int a = 10, b = 20;

        System.out.println(a == b);  // false
        System.out.println(a != b);  // true
        System.out.println(a > b);   // false
        System.out.println(a < b);   // true
        System.out.println(a >= b);  // false
        System.out.println(a <= b);  // true


//        Use Case
//        Relational operators are mostly used in:
//
//            *  if statements
//            *  while/for loops
//            *  Comparisons in conditions







    }


}
