package ex_08_Increment_Decrement;

public class Lab004_Pre_Post_Increment_02 {
    public static void main(String[] args) {



        int a = 10;
        int post = a--;
        System.out.println(post);  // output 10
        System.out.println(a);     // output 9


        System.out.println(".......................");

        int b = 10;
        int i = --b;
        System.out.println(i);  // output  9
        System.out.println(b);     // output  9




           }


}
